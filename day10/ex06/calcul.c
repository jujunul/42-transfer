#include "fonctions.h"

int		add(int a, int b)
{
	return (a + b);
}

int		less(int a, int b)
{
	return (a - b);
}

int		mod(int a, int b)
{
	return (a % b);
}

int		div(int a, int b)
{
	return (a / b);
}

int		multi(int a, int b)
{
	return (a * b);
}
